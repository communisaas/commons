/**
 * Generate src/lib/core/location/tz-country.ts — the complete IANA timezone →
 * ISO 3166-1 country map used as a low-confidence, country-level location
 * fallback when IP geolocation is unavailable.
 *
 * Source of truth: the system IANA Time Zone Database `zone.tab` (one principal
 * country per zone). This avoids a hand-maintained allowlist that silently omits
 * countries (the old table missed Malaysia, among others).
 *
 * Usage:  node scripts/generate-tz-country.mjs
 *
 * Re-run after a tzdata bump. zone.tab lives at /usr/share/zoneinfo/zone.tab on
 * macOS and most Linux distros; override with TZ_TAB=/path/to/zone.tab.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const TAB = process.env.TZ_TAB || '/usr/share/zoneinfo/zone.tab';
const OUT = resolve(__dirname, '../src/lib/core/location/tz-country.ts');
const VERSION_FILE = process.env.TZ_VERSION_FILE || '/usr/share/zoneinfo/+VERSION';

let version = '';
try {
	version = readFileSync(VERSION_FILE, 'utf8').trim();
} catch {
	// +VERSION is not present everywhere; provenance just falls back to the file name.
}

const lines = readFileSync(TAB, 'utf8')
	.split('\n')
	.filter((l) => l && !l.startsWith('#'));

const map = {};
for (const line of lines) {
	const cols = line.split('\t');
	// zone.tab carries one country code; zone1970.tab carries a comma-separated
	// list (principal country first). Take the first so either file works.
	const cc = (cols[0] || '').split(',')[0].trim();
	const zone = (cols[2] || '').trim();
	if (zone && /^[A-Z]{2}$/.test(cc)) map[zone] = cc;
}

const zones = Object.keys(map).sort();
const entries = zones.map((z) => `\t'${z}': '${map[z]}'`).join(',\n');
const provenance = version ? `IANA tzdata ${version}` : 'IANA tzdata (zone.tab)';

const out = `/**
 * Timezone → ISO 3166-1 alpha-2 country map.
 *
 * GENERATED by scripts/generate-tz-country.mjs from the authoritative IANA Time
 * Zone Database (zone.tab). Do not edit entries by hand — regenerate so coverage
 * stays complete. Source: ${provenance}. Each IANA zone maps to the single
 * principal country it sits in (zone.tab's one-country-per-zone convention).
 *
 * Used only as a low-confidence, country-level fallback when IP geolocation is
 * unavailable; timezone cannot reliably infer sub-national location (one zone
 * spans many states/provinces and is shared across countries at the same
 * offset), so this is country granularity by design.
 */
export const TIMEZONE_TO_COUNTRY: Record<string, string> = {
${entries}
};

/**
 * Resolve an IANA timezone (e.g. from Intl.DateTimeFormat().resolvedOptions())
 * to an ISO 3166-1 alpha-2 country code. Returns null for unknown zones.
 */
export function timezoneToCountry(timeZone: string): string | null {
\treturn TIMEZONE_TO_COUNTRY[timeZone] ?? null;
}
`;

// Sanity floor: a truncated, header-only, or wrong-format source would parse to
// a near-empty map and silently clobber the committed ~400-zone file. Fail loud
// instead so a regenerate-from-broken-tzdata accident can't ship a coverage gap.
if (zones.length < 100) {
	throw new Error(
		`Refusing to write ${OUT}: only ${zones.length} zones parsed from ${TAB} (expected >400). Truncated or wrong-format source?`
	);
}

writeFileSync(OUT, out);
console.log(`Wrote ${zones.length} zones (${new Set(Object.values(map)).size} countries) to ${OUT}`);
console.log(`Provenance: ${provenance}`);
